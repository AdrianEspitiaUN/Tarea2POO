public class Ejercicio28 {
    public static void main(String[] args){
        //Utilicemos la sentencia switch dentro de un ciclo for
        int x;
        for(x=0; x<=2; x++){
            switch(x){
                case 0:
                    System.out.println("Caso 0");
                    break;
                case 1:
                    System.out.println("Caso 1");
                    break;
                case 2:
                    System.out.println("Caso 2");
                    break;
            }
        }
    }
}
